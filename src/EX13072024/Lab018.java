package EX13072024;

public class Lab018 {
    public static void main(String[] args) {
        // Formatting
        int age = 98;
        System.out.println("You age is -> " + age); // + means concatenating
        // %d ->  any integer - byte, short, int or long
        // %s -> String
        // %c -> char
        // %f -> float
        System.out.printf("You age is -> %s",age);
    }
}
