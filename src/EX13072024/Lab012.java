package EX13072024;

public class Lab012 {
    public static void main(String[] args) {
// ASCII characters vs UNICODE CHARACTER
        //byte b = 128; //-127 to 127

        char c = 'A';
        System.out.println(c);

        char c1 = '$';
        System.out.println(c1);

        char c2 = '\n';  // new line
        System.out.println(c2);

        char c3 = '\u1F6A';  //:)
        System.out.println(c3);
        // :), :(, :D .;) -> unicode chars

    }
}
